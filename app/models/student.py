class Student:
    def __init__(
        self,
        student_id,
        first_name,
        last_name,
        date_of_birth,
        class_name
    ):
        self.student_id = student_id
        self.first_name = first_name
        self.last_name = last_name
        self.date_of_birth = date_of_birth
        self.class_name = class_name